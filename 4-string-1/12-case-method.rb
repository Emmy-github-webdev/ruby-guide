#capitalize makes the first character in the string capital letter
puts "hello".capitalize
puts "hello world".capitalize

#upcase turn all the letters to upper case, downcase turn all the letter to lower case

puts "hello".upcase
puts "hello world".upcase

puts "HELLO".downcase
puts "HELLO WORLD".downcase

#swapcase swap the upper case and lower case respectively

puts "EMMAnuel".swapcase
